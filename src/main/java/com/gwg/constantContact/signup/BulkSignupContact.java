package com.gwg.constantContact.signup;

public class BulkSignupContact {
	private String accessToken;
	private String bulkImportContacts;
	
	public String getAccessToken() {
		return accessToken;
	}
	public void setAccessToken(String accessToken) {
		this.accessToken = accessToken;
	}
	public String getBulkImportContacts() {
		return bulkImportContacts;
	}
	public void setBulkImportContacts(String bulkImportContacts) {
		this.bulkImportContacts = bulkImportContacts;
	}

}
